-- New workspace: prompt for a directory path, then open it as an ad-hoc workspace.
local CREATE_WORKSPACE = wezterm.action_callback(function(window, pane)
  window:perform_action(act.PromptInputLine {
    description = "New workspace — directory path (will be created if missing):",
    action = wezterm.action_callback(function(w2, p3, line)
      if line and line ~= "" then open_adhoc(w2, p3, line) end
    end),
  }, pane)
end)

-- Close workspace: confirm first, then close every tab (= closes the window).
local CLOSE_WORKSPACE = wezterm.action_callback(function(window, pane)
  local ws = window:active_workspace()
  window:perform_action(act.PromptInputLine {
    description = "Close workspace '" .. ws .. "'?  type y to confirm:",
    action = wezterm.action_callback(function(w, _p, line)
      if line ~= "y" and line ~= "Y" then return end
      local tabs = w:mux_window():tabs()
      for i = #tabs, 1, -1 do
        local p2 = tabs[i]:active_pane()
        if p2 then
          w:perform_action(act.CloseCurrentTab { confirm = false }, p2)
        end
      end
    end),
  }, pane)
end)

-- ─────────────────────────────────────────────────────────────────────────────
-- Split undo stack
-- Every tracked split pushes the new pane's ID onto the stack (up to MAX_UNDO).
-- LEADER+U pops the most recent ID and closes that pane, effectively undoing it.
-- ─────────────────────────────────────────────────────────────────────────────
local split_undo_stack = {}
local MAX_UNDO = 10

local function split_and_track(direction)
  local split_action = direction == "Right"
    and act.SplitHorizontal { domain = "CurrentPaneDomain" }
    or  act.SplitVertical   { domain = "CurrentPaneDomain" }
  return wezterm.action_callback(function(window, pane)
    window:perform_action(split_action, pane)
    -- Wait one tick for the split to land, then record the focused pane.
    wezterm.time.call_after(0.05, function()
      local new_pane = window:active_pane()
      if new_pane then
        table.insert(split_undo_stack, new_pane:pane_id())
        if #split_undo_stack > MAX_UNDO then
          table.remove(split_undo_stack, 1)
        end
      end
    end)
  end)
end

local SPLIT_RIGHT = split_and_track("Right")
local SPLIT_DOWN  = split_and_track("Down")

local UNDO_SPLIT = wezterm.action_callback(function(window, _pane)
  if #split_undo_stack == 0 then
    window:toast_notification("WezTerm", "Nothing to undo", nil, 1500)
    return
  end
  local target_id = table.remove(split_undo_stack)  -- pop most recent
  for _, tab in ipairs(window:mux_window():tabs()) do
    for _, p in ipairs(tab:panes()) do
      if p:pane_id() == target_id then
        p:activate()
        window:perform_action(act.CloseCurrentPane { confirm = false }, p)
        return
      end
    end
  end
  -- Already closed manually — notify and let caller retry if needed.
  window:toast_notification("WezTerm", "Pane already closed", nil, 1500)
end)

