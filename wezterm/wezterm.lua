local mux     = wezterm.mux

-- Config directory (sibling files live here)
local CFG_DIR     = wezterm.config_dir
local STATE_FILE  = CFG_DIR .. "/session_state.lua"
local PROJECTS    = dofile(CFG_DIR .. "/projects.lua")

local BASH = "C:/Program Files/Git/bin/bash.exe"

-- Default split layout applied when a tab's layout field is nil.
-- Override per-tab in projects.lua with layout = "vsplit"|"hsplit"|"none".
local DEFAULT_LAYOUTS = {
  agent = "vsplit",   -- two side-by-side panes
  dev   = "hsplit",   -- primary top, secondary bottom
  cmd   = "hsplit",
  test  = "hsplit",
  sys   = "hsplit",
}

-- Root folders scanned for immediate subdirectories in the workspace picker.
-- Repos found here that aren't in PROJECTS appear as an "ad-hoc" section.
local REPO_ROOTS = {
