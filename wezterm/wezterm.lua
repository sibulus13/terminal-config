-- ─────────────────────────────────────────────────────────────────────────────
-- Status bar
-- Left : currently OPEN workspaces only — active=crimson, other=muted, ●=attention
-- Right: persistent legend hint + clock
-- ─────────────────────────────────────────────────────────────────────────────

wezterm.on("update-right-status", function(window, _pane)
  local current_ws = window:active_workspace()

  -- Collect ALL open workspaces keyed by id
  local open_wins = {}
  for _, win in ipairs(mux.all_windows()) do
    open_wins[win:get_workspace()] = win
  end

  -- Build ordered list: known projects first (projects.lua order), then
  -- ad-hoc / launcher workspaces that aren't in PROJECTS.
  local ordered = {}
  local shown   = {}
  for _, proj in ipairs(PROJECTS) do
    if open_wins[proj.id] then
      table.insert(ordered, { id = proj.id, label = proj.label, win = open_wins[proj.id] })
      shown[proj.id] = true
    end
  end
  for ws_id, win in pairs(open_wins) do
    if not shown[ws_id] then
      table.insert(ordered, { id = ws_id, label = ws_id, win = win })
    end
  end

  local left_parts = { { Text = " " } }

  for _, entry in ipairs(ordered) do
    local win       = entry.win
    local is_active = entry.id == current_ws
    local attention = false
    for _, tab in ipairs(win:tabs()) do
      for _, p in ipairs(tab:panes()) do
        if p:has_unseen_output() then attention = true; break end
      end
      if attention then break end
    end

    local label = string.format(" %s%s ", attention and "● " or "", entry.label)

    if is_active then
      table.insert(left_parts, { Attribute = { Intensity = "Bold" } })
      table.insert(left_parts, { Foreground = { Color = "#e8378e" } })   -- crimson: focused
    elseif attention then
      table.insert(left_parts, { Attribute = { Intensity = "Normal" } })
      table.insert(left_parts, { Foreground = { Color = "#d4943a" } })   -- amber: needs attention
    else
      table.insert(left_parts, { Attribute = { Intensity = "Normal" } })
      table.insert(left_parts, { Foreground = { Color = "#9b6aaa" } })   -- muted rose: open
    end
    table.insert(left_parts, { Text = label })
  end

  -- Nothing open yet: nudge user toward the picker
  if #ordered == 0 then
    table.insert(left_parts, { Attribute = { Intensity = "Bold" } })
    table.insert(left_parts, { Foreground = { Color = "#c4185c" } })
    table.insert(left_parts, { Text = " ALT+P" })
    table.insert(left_parts, { Foreground = { Color = "#6b3080" } })
    table.insert(left_parts, { Text = " open a workspace " })
  end

  window:set_left_status(wezterm.format(left_parts))

  -- Right: save indicator ◆ | legend hint | clock
  local time = wezterm.strftime "%H:%M"
  local right_parts = { { Attribute = { Intensity = "Normal" } } }
  if last_save_time then
    table.insert(right_parts, { Foreground = { Color = "#1f8c5a" } })
    table.insert(right_parts, { Text = "◆ " .. last_save_time })
    table.insert(right_parts, { Foreground = { Color = "#3a1a4a" } })
    table.insert(right_parts, { Text = "  │  " })
  end
  table.insert(right_parts, { Foreground = { Color = "#6b3080" } })
  table.insert(right_parts, { Text = "ALT+/  legend" })
  table.insert(right_parts, { Foreground = { Color = "#3a1a4a" } })
  table.insert(right_parts, { Text = "  │  " })
  table.insert(right_parts, { Foreground = { Color = "#6b3080" } })
  table.insert(right_parts, { Text = time .. "  " })
  window:set_right_status(wezterm.format(right_parts))
end)

-- ─────────────────────────────────────────────────────────────────────────────
-- Attention tracking
-- bell_panes[pane_id] = true when a pane has rung the bell (agent stopped).
-- Cleared when the user focuses that tab. This is the semantic "done" signal —
-- distinct from has_unseen_output which fires on every line of continuous output.
-- ─────────────────────────────────────────────────────────────────────────────

local bell_panes = {}

-- Bell fires when a pane emits \a — wired to Claude Code Stop hook and similar.
-- Shows a toast identifying the workspace and tab so you know exactly where to look.
wezterm.on("bell", function(window, pane)
  bell_panes[pane:pane_id()] = true
  local tab   = pane:tab()
  local label = tab and tab:get_title() or "terminal"
  local ws    = window:active_workspace()
  window:toast_notification(
    ws .. "  ·  " .. label,
    "Agent finished — ready for input",
    nil,
    5000
  )
end)

-- ─────────────────────────────────────────────────────────────────────────────
-- Tab title renderer
-- Four visual states:
--   bright  (#c0caf5)  — active, you are here
--   amber   (#d4943a)  — bell rang here, agent done and waiting (clear on focus)
--   warm    (#8a7aaa)  — has unseen output, agent still running
--   muted   (#565f89)  — idle
-- ─────────────────────────────────────────────────────────────────────────────

wezterm.on("format-tab-title", function(tab, _tabs, _panes, _cfg, _hover, max_width)
  local title = tab.tab_title
  if not title or #title == 0 then title = tab.active_pane.title end
  if #title > max_width - 4 then
    title = wezterm.truncate_right(title, max_width - 4)
  end
  local idx     = tab.tab_index + 1
  local pane_id = tab.active_pane.pane_id

  -- Clear bell flag when the user arrives at this tab
  if tab.is_active then bell_panes[pane_id] = nil end

  local fg
  if tab.is_active then
    fg = "#c0caf5"
  elseif bell_panes[pane_id] then
    fg = "#d4943a"                       -- amber: agent done, needs response
  elseif tab.active_pane.has_unseen_output then
    fg = "#8a7aaa"                       -- warm purple: running, producing output
  else
    fg = "#565f89"                       -- muted: idle
  end

  return {
    { Background = { Color = tab.is_active and "#1a1b26" or "#16161e" } },
    { Foreground = { Color = fg } },
    { Text = " " .. idx .. ":" .. title .. " " },
  }
end)

