}

-- ─────────────────────────────────────────────────────────────────────────────
-- Session state helpers
-- Read: dofile the auto-generated state table (or return nil if missing/invalid)
-- Write: serialize the current workspace+tab layout to a Lua return table
-- ─────────────────────────────────────────────────────────────────────────────

local function read_session_state()
  local ok, result = pcall(dofile, STATE_FILE)
  if ok and type(result) == "table" then return result end
  return nil
end

local function serialize_value(v, indent)
  indent = indent or ""
  local t = type(v)
  if t == "nil"     then return "nil"
  elseif t == "boolean" then return tostring(v)
  elseif t == "number"  then return tostring(v)
  elseif t == "string"  then return string.format("%q", v)
  elseif t == "table"   then
    local inner = indent .. "  "
    local parts = {}
    local is_array = #v > 0
    if is_array then
      for _, item in ipairs(v) do
        table.insert(parts, inner .. serialize_value(item, inner))
      end
      return "{\n" .. table.concat(parts, ",\n") .. "\n" .. indent .. "}"
    else
      for k, val in pairs(v) do
        local key = type(k) == "string"
            and ("[" .. string.format("%q", k) .. "]")
            or  ("[" .. tostring(k) .. "]")
        table.insert(parts, inner .. key .. " = " .. serialize_value(val, inner))
      end
      return "{\n" .. table.concat(parts, ",\n") .. "\n" .. indent .. "}"
    end
  end
  return "nil"
end

local function write_session_state(state)
  local f = io.open(STATE_FILE, "w")
  if not f then return end
  f:write("-- auto-generated by wezterm — do not hand-edit\n")
  f:write("return " .. serialize_value(state) .. "\n")
  f:close()
end

-- Capture current layout across all windows/workspaces
local function capture_state()
  local state = { active_workspace = mux.get_active_workspace(), workspaces = {} }
  for _, win in ipairs(mux.all_windows()) do
    local ws = win:get_workspace()
    if not state.workspaces[ws] then
      local tabs_state   = {}
      local active_tab_id = win:active_tab():tab_id()
      local active_tab_idx = 0
      for tab_idx, tab in ipairs(win:tabs()) do
        local pane    = tab:active_pane()
        local cwd_uri = pane:get_current_working_dir()
        local cwd_str = cwd_uri and cwd_uri.file_path or ""
        local resume_cmd = nil
        for _, proj in ipairs(PROJECTS) do
          if proj.id == ws and proj.tabs[tab_idx] and proj.tabs[tab_idx].resume then
            resume_cmd = proj.tabs[tab_idx].cmd
          end
        end
        if tab:tab_id() == active_tab_id then
          active_tab_idx = tab_idx - 1  -- 0-based, matches ActivateTab(n)
        end
        table.insert(tabs_state, {
          title      = tab:get_title(),
          cwd        = cwd_str,
          resume_cmd = resume_cmd,
        })
      end
      state.workspaces[ws] = {
        active_tab_idx = active_tab_idx,
        tabs           = tabs_state,
      }
    end
  end
  return state
end

-- ─────────────────────────────────────────────────────────────────────────────
-- Startup: open a single plain shell. Workspaces are spawned on-demand.
-- ─────────────────────────────────────────────────────────────────────────────

local HELP_SCRIPT = CFG_DIR:gsub("\\", "/") .. "/help.sh"

wezterm.on("gui-startup", function(_cmd)
  -- Start on the help tab so key bindings are immediately visible.
  -- Press ALT+1-7 to open a workspace; that workspace becomes the active window.
  -- This launcher window stays open as a scratch shell / reference.
  local help_tab, _, window = mux.spawn_window({
    workspace = "launcher",
    args      = { BASH, HELP_SCRIPT },
  })
  help_tab:set_title("help")
  local shell_tab, _ = window:spawn_tab({ args = { BASH, "-l" } })
  shell_tab:set_title("shell")
  help_tab:activate()
end)

-- ─────────────────────────────────────────────────────────────────────────────
-- open_project: switch to a workspace, spawning it with full tab layout if new.
-- ─────────────────────────────────────────────────────────────────────────────

local function workspace_exists(id)
  for _, win in ipairs(mux.all_windows()) do
    if win:get_workspace() == id then return true end
  end
  return false
end

-- Apply pane split to a freshly spawned tab based on layout field.
local function apply_split(tab, layout_override, tab_title, cwd)
  local layout = layout_override
  if layout == nil then
    layout = DEFAULT_LAYOUTS[tab_title]
  end
  if not layout or layout == "none" then return end

  local direction = layout == "vsplit" and "Right" or "Down"
  tab:active_pane():split({ direction = direction, cwd = cwd, args = { BASH, "-l" } })
  -- Return focus to the first (main) pane
  tab:active_pane():activate()
end

local function open_project(proj_id)
  return wezterm.action_callback(function(window, pane)
    if workspace_exists(proj_id) then
      window:perform_action(act.SwitchToWorkspace { name = proj_id }, pane)
      return
    end

    local proj = nil
    for _, p in ipairs(PROJECTS) do
      if p.id == proj_id then proj = p; break end
    end
    if not proj then return end

    local saved   = read_session_state()
    local t1      = proj.tabs[1]
    local t1_args = t1.cmd and { BASH, "-lc", t1.cmd } or { BASH, "-l" }
    if saved and saved.workspaces and saved.workspaces[proj_id] then
      local st = saved.workspaces[proj_id].tabs and saved.workspaces[proj_id].tabs[1]
      if st and st.resume_cmd then t1_args = { BASH, "-lc", st.resume_cmd } end
    end

    -- Switch the CURRENT OS window to the new workspace.
    -- `spawn` seeds the workspace's first tab — no new OS window is created.
    window:perform_action(act.SwitchToWorkspace {
      name  = proj_id,
      spawn = { cwd = proj.cwd, args = t1_args },
    }, pane)

    -- perform_action is asynchronous; wait briefly for the switch to land
    -- before we can manipulate the workspace's tabs via the mux API.
    wezterm.time.call_after(0.15, function()
      local target_win = nil
      for _, win in ipairs(mux.all_windows()) do
        if win:get_workspace() == proj_id then target_win = win; break end
      end
      if not target_win then return end

      local tabs = target_win:tabs()
      if #tabs == 0 then return end

      tabs[1]:set_title(t1.title)
      apply_split(tabs[1], t1.layout, t1.title, proj.cwd)

      for j = 2, #proj.tabs do
        local t    = proj.tabs[j]
        local args = t.cmd and { BASH, "-lc", t.cmd } or { BASH, "-l" }
        if saved and saved.workspaces and saved.workspaces[proj_id] then
          local st = saved.workspaces[proj_id].tabs and saved.workspaces[proj_id].tabs[j]
          if st and st.resume_cmd then args = { BASH, "-lc", st.resume_cmd } end
        end
        local new_tab = target_win:spawn_tab({ cwd = proj.cwd, args = args })
        new_tab:set_title(t.title)
        apply_split(new_tab, t.layout, t.title, proj.cwd)
      end

      -- Restore the tab that was active when this workspace was last closed
      local restore_idx = 0
      if saved and saved.workspaces and saved.workspaces[proj_id] then
        restore_idx = saved.workspaces[proj_id].active_tab_idx or 0
      end
      local all_tabs = target_win:tabs()
      local target_tab = all_tabs[restore_idx + 1] or all_tabs[1]
      if target_tab then target_tab:activate() end
    end)
  end)
end

-- Open any directory path as a workspace (ad-hoc, not in projects.lua).
-- Creates the directory if it doesn't exist, then switches to a new workspace
-- with a single bash shell. The workspace id is derived from the dir basename.
local function open_adhoc(window, pane, dir_path)
  -- normalize slashes, strip trailing separator
  dir_path = dir_path:gsub("\\", "/"):gsub("/+$", "")
  local basename = dir_path:match("([^/]+)$") or dir_path
  local ws_id = basename:lower():gsub("[^%w%-]", "-")

  wezterm.run_child_process({
    "powershell.exe", "-NoProfile", "-Command",
    "New-Item -ItemType Directory -Force -Path '" .. dir_path .. "' | Out-Null",
  })

  if workspace_exists(ws_id) then
    window:perform_action(act.SwitchToWorkspace { name = ws_id }, pane)
    return
  end

  window:perform_action(act.SwitchToWorkspace {
    name  = ws_id,
    spawn = { cwd = dir_path, args = { BASH } },
  }, pane)
end

-- Workspace picker: defined once, shared by both LEGEND and config.keys.
-- Order: [+] new (top) | pinned projects | discovered repos from REPO_ROOTS.
local PICK_WORKSPACE = wezterm.action_callback(function(window, pane)
  -- [+] New workspace always first so it's reachable without scrolling
  local choices = {
    { id = "__new__", label = "[+]  New workspace  (type a path)" },
    { id = "",        label = "─────────────────────────────────────────" },
  }

  -- Pinned projects
  local known_cwds = {}
  for _, p in ipairs(PROJECTS) do
    known_cwds[p.cwd] = true
    local open = workspace_exists(p.id)
    table.insert(choices, {
      id    = p.id,
      label = (open and "● " or "○ ") .. p.label,
    })
  end

  -- Discovered repos: immediate subdirs of REPO_ROOTS not already in PROJECTS.
  -- Glob on Windows doesn't reliably filter dirs-only via trailing slash, so
  -- skip anything with a file extension (repos don't have them; loose files do).
  local discovered = {}
  for _, root in ipairs(REPO_ROOTS) do
    for _, d in ipairs(wezterm.glob(root .. "/*")) do
      local path = d:gsub("[\\/]+$", "")
      local name = path:match("([^/\\]+)$") or ""
      local has_ext = name:match("%.[^./\\]+$")
      if not has_ext and not known_cwds[path] then
        known_cwds[path] = true
        table.insert(discovered, path)
      end
    end
  end

  if #discovered > 0 then
    table.insert(choices, { id = "", label = "── repos ────────────────────────────────" })
    for _, path in ipairs(discovered) do
      local name = path:match("([^/\\]+)$") or path
      table.insert(choices, {
        id    = "__path__:" .. path,
        label = "○ " .. name,
      })
    end
  end

  window:perform_action(act.InputSelector {
    title   = "Workspace  (● open · ○ closed)",
    choices = choices,
    fuzzy   = true,
    action  = wezterm.action_callback(function(w, p2, id, _)
      if not id or id == "" then return end
      if id == "__new__" then
        w:perform_action(act.PromptInputLine {
          description = "Directory path (will be created if missing):",
          action = wezterm.action_callback(function(w2, p3, line)
            if line and line ~= "" then open_adhoc(w2, p3, line) end
          end),
        }, p2)
      else
        local repo_path = id:match("^__path__:(.+)$")
        if repo_path then
          open_adhoc(w, p2, repo_path)
        else
          w:perform_action(open_project(id), p2)
        end
      end
    end),
  }, pane)
end)

-- ─────────────────────────────────────────────────────────────────────────────
-- Periodic state flush (every 30s) so abrupt kills don't lose context
-- ─────────────────────────────────────────────────────────────────────────────

local function schedule_state_flush()
  wezterm.time.call_after(30, function()
    write_session_state(capture_state())
    schedule_state_flush() -- reschedule indefinitely
  end)
end

wezterm.on("gui-attached", function(_domain)
  schedule_state_flush()
end)

-- Also flush on graceful close
wezterm.on("window-close-requested", function(window, _pane)
  write_session_state(capture_state())
  window:perform_action(act.CloseCurrentWindow, window:active_pane())
end)

